"""
Программа вычисления факториала введённого пользователем числа.

Модуль запрашивает у пользователя положительное целое число,
проверяет корректность ввода и выводит значение факториала.
Для вычисления используется math.factorial — реализация на языке C,
которая работает существенно быстрее и надёжнее, чем ручная
рекурсивная или циклическая реализация на больших числах.
"""

import math
import sys


def get_positive_integer(prompt: str = "Введите положительное целое число: ") -> int:
    """
    Запрашивает у пользователя ввод и возвращает положительное целое число.

    Повторяет запрос до тех пор, пока не будет введено корректное значение.
    Обрабатывает следующие ошибки ввода:
      - нечисловые данные (буквы, спецсимволы, пустая строка);
      - дробные числа;
      - ноль и отрицательные числа.

    :param prompt: текст приглашения к вводу
    :return: положительное целое число, введённое пользователем
    """
    while True:
        raw_value = input(prompt).strip()
        try:
            # int() сам по себе не примет "3.5" или "abc" — это и нужно
            value = int(raw_value)
        except ValueError:
            print(
                f'Ошибка: "{raw_value}" не является целым числом. '
                "Попробуйте снова."
            )
            continue

        if value <= 0:
            print(
                f"Ошибка: число должно быть положительным (введено {value}). "
                "Попробуйте снова."
            )
            continue

        return value


def factorial(n: int) -> int:
    """
    Вычисляет факториал числа n.

    Использует math.factorial — оптимизированную встроенную реализацию,
    которая эффективно работает даже с очень большими числами
    (например, 5000! вычисляется практически мгновенно).

    :param n: неотрицательное целое число
    :raises ValueError: если n отрицательное
    :return: n!
    """
    if n < 0:
        raise ValueError("Факториал отрицательного числа не определён")
    return math.factorial(n)


def main() -> None:
    try:
        number = get_positive_integer()
        result = factorial(number)
        print(f"\nФакториал числа {number} равен:\n{result}")
    except KeyboardInterrupt:
        print("\nВыполнение прервано пользователем.")
        sys.exit(0)


if __name__ == "__main__":
    main()

input("Нажмите Enter для выхода...")
